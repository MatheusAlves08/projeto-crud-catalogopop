namespace CatalogoPOP.Domain.Enums;

/// <summary>
/// Enum (Enumerador) que define os departamentos fixos disponíveis no sistema.
/// Usar um Enum evita erros de digitação e garante que apenas essas opções sejam selecionadas.
/// </summary>
public enum Departamento
{
    Qualidade,
    Manutencao,
    Logistica,
    Administrativo,
    Producao,
    TI,
    RH
}
